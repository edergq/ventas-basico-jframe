package Entidades;

public class Trabajador extends Persona{
    
    private String s_idTrabajador;
    private String s_codigoTrabajador;
    private String s_areaTrabajador;
    private String s_usuario;
    private String s_password;
    private String s_estado;
    private String s_rol;

    public String getS_idTrabajador() {
        return s_idTrabajador;
    }

    public void setS_idTrabajador(String s_idTrabajador) {
        this.s_idTrabajador = s_idTrabajador;
    }

    public String getS_codigoTrabajador() {
        return s_codigoTrabajador;
    }

    public void setS_codigoTrabajador(String s_codigoTrabajador) {
        this.s_codigoTrabajador = s_codigoTrabajador;
    }

    public String getS_areaTrabajador() {
        return s_areaTrabajador;
    }

    public void setS_areaTrabajador(String s_areaTrabajador) {
        this.s_areaTrabajador = s_areaTrabajador;
    }

    public String getS_usuario() {
        return s_usuario;
    }

    public void setS_usuario(String s_usuario) {
        this.s_usuario = s_usuario;
    }

    public String getS_password() {
        return s_password;
    }

    public void setS_password(String s_password) {
        this.s_password = s_password;
    }

    public String getS_estado() {
        return s_estado;
    }

    public void setS_estado(String s_estado) {
        this.s_estado = s_estado;
    }

    public String getS_rol() {
        return s_rol;
    }

    public void setS_rol(String s_rol) {
        this.s_rol = s_rol;
    }
    
    
    

}
